// NT status codes used in broker responses
#ifndef STATUS_SUCCESS
#define STATUS_SUCCESS               ((NTSTATUS)0x00000000L)
#define STATUS_UNSUCCESSFUL          ((NTSTATUS)0xC0000001L)
#define STATUS_INVALID_HANDLE        ((NTSTATUS)0xC0000008L)
#define STATUS_INVALID_PARAMETER     ((NTSTATUS)0xC000000DL)
#define STATUS_NO_MEMORY             ((NTSTATUS)0xC0000017L)
#define STATUS_ACCESS_DENIED         ((NTSTATUS)0xC0000022L)
#define STATUS_OBJECT_NAME_INVALID   ((NTSTATUS)0xC0000033L)
#define STATUS_OBJECT_NAME_NOT_FOUND ((NTSTATUS)0xC0000034L)
#define STATUS_OBJECT_PATH_NOT_FOUND ((NTSTATUS)0xC000003AL)
#endif

// Map common Win32 error codes to NTSTATUS equivalents for broker responses
// generated by Win32 API failures (ImpersonateLoggedOnUser, DuplicateHandle, etc.).
static NTSTATUS BrokerWin32ToNtStatus(DWORD err) {
    switch (err) {
        case ERROR_SUCCESS:           return STATUS_SUCCESS;
        case ERROR_ACCESS_DENIED:     return STATUS_ACCESS_DENIED;
        case ERROR_INVALID_PARAMETER: return STATUS_INVALID_PARAMETER;
        case ERROR_NOT_ENOUGH_MEMORY: return STATUS_NO_MEMORY;
        case ERROR_INVALID_HANDLE:    return STATUS_INVALID_HANDLE;
        case ERROR_FILE_NOT_FOUND:    return STATUS_OBJECT_NAME_NOT_FOUND;
        case ERROR_PATH_NOT_FOUND:    return STATUS_OBJECT_PATH_NOT_FOUND;
        default:                      return STATUS_UNSUCCESSFUL;
    }
}

