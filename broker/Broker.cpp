DWORD Broker::HandleCreateReg(const BYTE* pPayload, DWORD dwPayloadSize, DWORD dwRequestId,
                               DWORD dwCallerPid, BYTE* pResp, DWORD, DWORD& dwRespSize) {
    auto* pRespHeader = reinterpret_cast<BrokerResponseHeader*>(pResp);

    // Validate payload
    if (dwPayloadSize < offsetof(CreateRegRequestPayload, szData))
        { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }

    auto* pReq = reinterpret_cast<const CreateRegRequestPayload*>(pPayload);

    DWORD dwPathChars  = pReq->dwPathLengthBytes / sizeof(WCHAR);
    DWORD dwClassChars = pReq->dwClassLengthBytes / sizeof(WCHAR);
    DWORD dwTotalChars = dwPathChars + dwClassChars;
    DWORD dwAvailChars = (dwPayloadSize - offsetof(CreateRegRequestPayload, szData)) / sizeof(WCHAR);

    if (dwTotalChars > dwAvailChars || dwPathChars == 0)
        { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }

    LPCWSTR szNtPath = pReq->szData;
    if (szNtPath[dwPathChars - 1] != L'\0')
        { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }

    LPCWSTR szClass = nullptr;
    if (dwClassChars > 0) {
        szClass = pReq->szData + dwPathChars;
        if (szClass[dwClassChars - 1] != L'\0')
            { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }
    }

    // Policy check using the full NT registry path → Win32 hive format
    WCHAR szWin32Path[512];
    if (!NtRegPathToWin32ForPolicy(szNtPath, szWin32Path, _countof(szWin32Path)))
        { pRespHeader->dwStatus = (DWORD)STATUS_OBJECT_NAME_INVALID; return ERROR_BAD_PATHNAME; }

    if (!m_policy.IsAllowed(BROKER_OP_CREATE_REG, szWin32Path)) {
        pRespHeader->dwStatus = (DWORD)STATUS_ACCESS_DENIED;
        return ERROR_ACCESS_DENIED;
    }

    BROKER_UNICODE_STRING uPath;
    BrokerInitUnicodeString(&uPath, szNtPath);
    BROKER_OBJECT_ATTRIBUTES oa;
    BROKER_INIT_OA(&oa, &uPath, pReq->ObjAttributes | OBJ_CASE_INSENSITIVE);

    BROKER_UNICODE_STRING uClass;
    BROKER_UNICODE_STRING* pClass = nullptr;
    if (szClass) {
        BrokerInitUnicodeString(&uClass, szClass);
        pClass = &uClass;
    }

    HANDLE hKey = nullptr;
    ULONG disposition = 0;

    // Impersonate and create
    if (!ImpersonateLoggedOnUser(m_hBrokerToken))
        { pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(GetLastError()); return pRespHeader->dwStatus; }

    NTSTATUS ntStatus = s_NtCreateKey(&hKey, pReq->DesiredAccess, &oa,
                                       pReq->TitleIndex, pClass, pReq->CreateOptions,
                                       &disposition);

    RevertToSelf();

    DWORD fStatus = NtToDosError(ntStatus);
    if (fStatus != ERROR_SUCCESS) {
        pRespHeader->dwStatus = (DWORD)ntStatus;
        return fStatus;
    }

    // Duplicate the key handle into the caller's process
    HANDLE hCallerProcess = OpenProcess(PROCESS_DUP_HANDLE, FALSE, dwCallerPid);
    if (!hCallerProcess) {
        fStatus = GetLastError();
        CloseHandle(hKey);
        pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(fStatus);
        return fStatus;
    }

    HANDLE hDup = nullptr;
    if (!DuplicateHandle(GetCurrentProcess(), hKey, hCallerProcess, &hDup,
                         0, FALSE, DUPLICATE_SAME_ACCESS))
        fStatus = GetLastError();

    CloseHandle(hKey);
    CloseHandle(hCallerProcess);

    pRespHeader->dwTotalSize = sizeof(BrokerResponseHeader) + sizeof(CreateRegResponsePayload);
    pRespHeader->dwRequestId = dwRequestId;
    pRespHeader->dwStatus    = (DWORD)BrokerWin32ToNtStatus(fStatus);
    pRespHeader->dwHandle    = (DWORD_PTR)hDup;

    auto* pRespPayload = reinterpret_cast<CreateRegResponsePayload*>(pResp + sizeof(BrokerResponseHeader));
    pRespPayload->Disposition = disposition;
    dwRespSize = sizeof(BrokerResponseHeader) + sizeof(CreateRegResponsePayload);

    return fStatus;
}

DWORD Broker::HandleOpenProcess(const BYTE* pPayload, DWORD dwPayloadSize, DWORD dwRequestId,
                                DWORD dwCallerPid, BYTE* pResp, DWORD, DWORD& dwRespSize) {
    auto* pRespHeader = reinterpret_cast<BrokerResponseHeader*>(pResp);

    if (dwPayloadSize < sizeof(OpenProcessRequestPayload))
        { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }

    auto* pReq = reinterpret_cast<const OpenProcessRequestPayload*>(pPayload);

    // Policy check — use PID as a string for the path pattern
    WCHAR szPidStr[32];
    swprintf_s(szPidStr, _countof(szPidStr), L"%lu", pReq->dwTargetProcessId);

    if (!m_policy.IsAllowed(BROKER_OP_OPEN_PROCESS, szPidStr)) {
        pRespHeader->dwStatus = (DWORD)STATUS_ACCESS_DENIED;
        return ERROR_ACCESS_DENIED;
    }

    if (!ImpersonateLoggedOnUser(m_hBrokerToken))
        { pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(GetLastError()); return pRespHeader->dwStatus; }

    HANDLE hTarget = OpenProcess(pReq->dwDesiredAccess, FALSE, pReq->dwTargetProcessId);
    DWORD fStatus = hTarget ? ERROR_SUCCESS : GetLastError();

    RevertToSelf();

    if (fStatus != ERROR_SUCCESS) {
        pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(fStatus);
        return fStatus;
    }

    // Duplicate into caller
    HANDLE hCallerProcess = OpenProcess(PROCESS_DUP_HANDLE, FALSE, dwCallerPid);
    if (!hCallerProcess) {
        fStatus = GetLastError();
        CloseHandle(hTarget);
        pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(fStatus);
        return fStatus;
    }

    HANDLE hDup = nullptr;
    if (!DuplicateHandle(GetCurrentProcess(), hTarget, hCallerProcess, &hDup,
                         0, FALSE, DUPLICATE_SAME_ACCESS))
        fStatus = GetLastError();

    CloseHandle(hTarget);
    CloseHandle(hCallerProcess);

    pRespHeader->dwTotalSize = sizeof(BrokerResponseHeader);
    pRespHeader->dwRequestId = dwRequestId;
    pRespHeader->dwStatus    = (DWORD)BrokerWin32ToNtStatus(fStatus);
    pRespHeader->dwHandle    = (DWORD_PTR)hDup;
    dwRespSize = sizeof(BrokerResponseHeader);

    return fStatus;
}

DWORD Broker::HandleTrace(const BYTE* pPayload, DWORD dwPayloadSize, DWORD dwRequestId,
                          DWORD dwCallerPid, BYTE* pResp, DWORD, DWORD& dwRespSize) {
    auto* pRespHeader = reinterpret_cast<BrokerResponseHeader*>(pResp);

    if (dwPayloadSize < offsetof(TraceRequestPayload, szMessage)) {
        pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER;
        return ERROR_INVALID_PARAMETER;
    }

    auto* pReq = reinterpret_cast<const TraceRequestPayload*>(pPayload);
    if (pReq->dwMessageLengthBytes == 0 ||
        pReq->dwMessageLengthBytes > dwPayloadSize - offsetof(TraceRequestPayload, szMessage)) {
        pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER;
        return ERROR_INVALID_PARAMETER;
    }

    DWORD msgLen = pReq->dwMessageLengthBytes;
    char message[1024] = {};
    size_t copyLen = msgLen < sizeof(message) ? msgLen : sizeof(message) - 1;
    memcpy(message, pReq->szMessage, copyLen);
    message[copyLen] = '\0';

    char fullMessage[1200] = {};
    sprintf_s(fullMessage, "Broker trace from pid=%lu: %s", dwCallerPid, message);
    IssueMessage(fullMessage, MSGTYPE_INFO);

    pRespHeader->dwTotalSize = sizeof(BrokerResponseHeader);
    pRespHeader->dwRequestId = dwRequestId;
    pRespHeader->dwStatus    = ERROR_SUCCESS;
    pRespHeader->dwHandle    = 0;
    dwRespSize = sizeof(BrokerResponseHeader);

    return ERROR_SUCCESS;
}
