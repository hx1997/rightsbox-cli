DWORD Broker::HandleOpenFile(const BYTE* pPayload, DWORD dwPayloadSize, DWORD dwRequestId,
                             DWORD dwCallerPid, BYTE* pResp, DWORD, DWORD& dwRespSize) {
    auto* pRespHeader = reinterpret_cast<BrokerResponseHeader*>(pResp);

    // Validate payload
    if (dwPayloadSize < offsetof(OpenFileRequestPayload, szNtPath))
        { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }

    auto* pReq = reinterpret_cast<const OpenFileRequestPayload*>(pPayload);

    if (!ValidateNtPath(pReq->szNtPath, pReq->dwPathLengthBytes, dwPayloadSize,
                        offsetof(OpenFileRequestPayload, szNtPath)))
        { pRespHeader->dwStatus = (DWORD)STATUS_INVALID_PARAMETER; return ERROR_INVALID_PARAMETER; }

    LPCWSTR szNtPath = pReq->szNtPath;

    // Policy check: convert NT path to Win32 format (for regex matching only)
    WCHAR szWin32Path[MAX_PATH];
    if (!NtPathToWin32ForPolicy(szNtPath, szWin32Path, MAX_PATH))
        { pRespHeader->dwStatus = (DWORD)STATUS_OBJECT_NAME_INVALID; return ERROR_BAD_PATHNAME; }

    if (!m_policy.IsAllowed(BROKER_OP_OPEN_FILE, szWin32Path)) {
        pRespHeader->dwStatus = (DWORD)STATUS_ACCESS_DENIED;
        LogBrokerFailure("OPEN_FILE", "policy denied", szWin32Path, ERROR_ACCESS_DENIED);
        return ERROR_ACCESS_DENIED;
    }

    // Build OBJECT_ATTRIBUTES from the raw NT path
    BROKER_UNICODE_STRING uPath;
    BrokerInitUnicodeString(&uPath, szNtPath);
    BROKER_OBJECT_ATTRIBUTES oa;
    BROKER_INIT_OA(&oa, &uPath, pReq->ObjAttributes | OBJ_CASE_INSENSITIVE);

    LARGE_INTEGER allocSize;
    allocSize.QuadPart = pReq->AllocationSize;

    BROKER_IO_STATUS_BLOCK iosb = {};
    HANDLE hFile = nullptr;

    // Impersonate and open
    if (!ImpersonateLoggedOnUser(m_hBrokerToken))
        { pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(GetLastError()); return pRespHeader->dwStatus; }

    NTSTATUS ntStatus = s_NtCreateFile(
        &hFile, pReq->DesiredAccess, &oa, &iosb,
        pReq->AllocationSize ? &allocSize : nullptr,
        pReq->FileAttributes ? pReq->FileAttributes : FILE_ATTRIBUTE_NORMAL,
        pReq->ShareAccess,
        pReq->CreateDisposition,
        pReq->CreateOptions,
        nullptr, 0);

    RevertToSelf();

    DWORD fStatus = NtToDosError(ntStatus);
    if (fStatus != ERROR_SUCCESS) {
        pRespHeader->dwStatus = (DWORD)ntStatus;
        // LogBrokerFailure("OPEN_FILE", "NtCreateFile failed", szNtPath, fStatus);
        return fStatus;
    }

    // Duplicate handle into caller's process
    HANDLE hCallerProcess = OpenProcess(PROCESS_DUP_HANDLE, FALSE, dwCallerPid);
    if (!hCallerProcess) {
        fStatus = GetLastError();
        CloseHandle(hFile);
        pRespHeader->dwStatus = (DWORD)BrokerWin32ToNtStatus(fStatus);
        // LogBrokerFailure("OPEN_FILE", "OpenProcess caller failed", szNtPath, fStatus);
        return fStatus;
    }

    HANDLE hDup = nullptr;
    if (!DuplicateHandle(GetCurrentProcess(), hFile, hCallerProcess, &hDup,
                         0, FALSE, DUPLICATE_SAME_ACCESS))
        fStatus = GetLastError();

    CloseHandle(hFile);
    CloseHandle(hCallerProcess);

    LogBrokerFailure("OPEN_FILE", fStatus == ERROR_SUCCESS ? "policy allowed" : "DuplicateHandle failed",
                     szWin32Path, fStatus);

    pRespHeader->dwTotalSize = sizeof(BrokerResponseHeader) + sizeof(OpenFileResponsePayload);
    pRespHeader->dwRequestId = dwRequestId;
    pRespHeader->dwStatus    = (DWORD)BrokerWin32ToNtStatus(fStatus);
    pRespHeader->dwHandle    = (DWORD_PTR)hDup;

    auto* pRespPayload = reinterpret_cast<OpenFileResponsePayload*>(pResp + sizeof(BrokerResponseHeader));
    pRespPayload->Information = (DWORD64)iosb.Information;
    dwRespSize = sizeof(BrokerResponseHeader) + sizeof(OpenFileResponsePayload);

    return fStatus;
}

