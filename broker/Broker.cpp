// ============================================================================
// Policy Path Helpers — NT namespace to Win32 format, for regex matching only
// ============================================================================

// Strips \??\ / \DosDevices\ / \??\\UNC\ prefix so policy patterns can use
// the familiar Win32 form (e.g. "C:\\Users\\.*").
static BOOL NtPathToWin32ForPolicy(LPCWSTR szNtPath, LPWSTR szOut, DWORD cchOut) {
    if (!szNtPath || !szNtPath[0] || !szOut || cchOut == 0)
        return FALSE;

    DWORD nLen = (DWORD)wcslen(szNtPath);

    // \??\UNC\server\share  →  \\server\share
    if (nLen >= 8 && _wcsnicmp(szNtPath, L"\\??\\UNC\\", 8) == 0) {
        DWORD n = nLen - 6; // keep two leading backslashes
        if (n >= cchOut) return FALSE;
        szOut[0] = L'\\'; szOut[1] = L'\\';
        wcsncpy_s(szOut + 2, cchOut - 2, szNtPath + 8, nLen - 8);
        return TRUE;
    }
    // \??\ prefix  →  Win32 drive path
    if (nLen >= 4 && wcsncmp(szNtPath, L"\\??\\", 4) == 0) {
        DWORD n = nLen - 4;
        if (n >= cchOut) return FALSE;
        wcsncpy_s(szOut, cchOut, szNtPath + 4, n);
        szOut[n] = L'\0';
        return TRUE;
    }
    // \DosDevices\UNC\  →  double backslashes (\\)
    if (nLen >= 16 && _wcsnicmp(szNtPath, L"\\DosDevices\\UNC\\", 16) == 0) {
        DWORD n = nLen - 14;
        if (n >= cchOut) return FALSE;
        szOut[0] = L'\\'; szOut[1] = L'\\';
        wcsncpy_s(szOut + 2, cchOut - 2, szNtPath + 16, nLen - 16);
        return TRUE;
    }
    // \DosDevices\  →  Win32 drive path
    if (nLen >= 12 && _wcsnicmp(szNtPath, L"\\DosDevices\\", 12) == 0) {
        DWORD n = nLen - 12;
        if (n >= cchOut) return FALSE;
        wcsncpy_s(szOut, cchOut, szNtPath + 12, n);
        szOut[n] = L'\0';
        return TRUE;
    }
    return FALSE;
}

// Converts NT registry path to Win32-style hive\subkey for policy matching.
// \Registry\Machine\X  →  HKLM\X
// \Registry\User\X     →  HKU\X
static BOOL NtRegPathToWin32ForPolicy(LPCWSTR szNtPath, LPWSTR szOut, DWORD cchOut) {
    if (!szNtPath) return FALSE;
    DWORD nLen = (DWORD)wcslen(szNtPath);

    struct { LPCWSTR prefix; DWORD prefLen; LPCWSTR hive; DWORD hiveLen; } maps[] = {
        { L"\\Registry\\Machine\\", 18, L"HKLM\\", 5 },
        { L"\\Registry\\User\\",    15, L"HKU\\",  4 },
    };
    for (auto& m : maps) {
        if (nLen > m.prefLen && _wcsnicmp(szNtPath, m.prefix, m.prefLen) == 0) {
            DWORD subLen = nLen - m.prefLen;
            if (m.hiveLen + subLen >= cchOut) return FALSE;
            wcscpy_s(szOut, cchOut, m.hive);
            wcsncpy_s(szOut + m.hiveLen, cchOut - m.hiveLen, szNtPath + m.prefLen, subLen);
            szOut[m.hiveLen + subLen] = L'\0';
            return TRUE;
        }
    }
    return FALSE;
}

static void LogBrokerFailure(const char* operation, const char* stage, LPCWSTR target, DWORD status) {
    char msg[512];
    sprintf_s(msg, "Broker: %s %s target=%ls status=%lu", operation, stage,
              target ? target : L"<null>", status);
    IssueMessage(msg, status == ERROR_SUCCESS ? MSGTYPE_INFO :
                      status == ERROR_ACCESS_DENIED ? MSGTYPE_WARNING : MSGTYPE_ERROR);
}

