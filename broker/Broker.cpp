    if (NT_SUCCESS(ntStatus)) {
        ntStatus = s_NtSetValueKey(hKey, &uValue, 0, pReq->dwRegType,
                                   (PVOID)pData, pReq->dwDataSize);
        CloseHandle(hKey);
    }

    RevertToSelf();

    DWORD fStatus = NtToDosError(ntStatus);

    pRespHeader->dwTotalSize = sizeof(BrokerResponseHeader);
    pRespHeader->dwRequestId = dwRequestId;
    pRespHeader->dwStatus    = (DWORD)ntStatus;
    pRespHeader->dwHandle    = 0;
    dwRespSize = sizeof(BrokerResponseHeader);

