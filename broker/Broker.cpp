// Validates that szNtPath is a non-empty, null-terminated NT path within the
// payload bounds, then extracts it.  Returns FALSE on any parse failure.
static BOOL ValidateNtPath(const WCHAR* szNtPath, DWORD dwPathLengthBytes,
                            DWORD dwPayloadSize, DWORD dwFixedSize) {
    if (dwPathLengthBytes < sizeof(WCHAR))
        return FALSE;
    if (dwPathLengthBytes > dwPayloadSize - dwFixedSize)
        return FALSE;
    DWORD chars = dwPathLengthBytes / sizeof(WCHAR);
    if (szNtPath[chars - 1] != L'\0')
        return FALSE;
    return TRUE;
}

